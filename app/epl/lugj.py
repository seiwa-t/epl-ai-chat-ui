# 変換テーブル（str.translate用）
_translate_table = str.maketrans(_char_map)

# ========== 保護語（変換対象外） ==========
# 固有名詞など、旧漢字のまま残すべき語句
_protected_words = [
    "當山",
    "當山くん",
]

# ========== ハングル検出 ==========
# ハングル音節ブロック: U+AC00〜U+D7AF
# ハングル互換字母: U+3130〜U+318F
_hangul_pattern = re.compile(r"[\uAC00-\uD7AF\u3130-\u318F]+")


def apply(text: str, user_rules: dict = None, user_protected: list = None) -> str:
    """
    LUGJフィルターを適用する。
    - 繁体字・簡体字 → 日本語常用漢字に変換
    - ハングル文字を除去
    - 保護語（固有名詞等）は変換しない
    - 意味・ロジック・人格・温度は一切変更しない
    """
    if not text:
        return text

    # Step 0: 保護語を退避（システム + ユーザー別）
    all_protected = list(_protected_words)
    if user_protected:
        all_protected.extend(user_protected)
    placeholders = {}
    for i, word in enumerate(all_protected):
        placeholder = f"\x00LUGJ_PROTECT_{i}\x00"
        if word in text:
            text = text.replace(word, placeholder)
            placeholders[placeholder] = word

    # Step 1: 繁体字・簡体字 → 日本語（システム辞書）
    result = text.translate(_translate_table)

    # Step 1.5: ユーザー別辞書の適用
    if user_rules:
        for old_char, new_char in user_rules.items():
            result = result.replace(old_char, new_char)

    # Step 2: ハングル処理
    # 一文字だけ混入 → 意図しない混入として除去
    # 連続する場合 → 意図的な使用として保持
    result = re.sub(r"(?<![가-힣])[\uAC00-\uD7AF](?![가-힣])", "", result)

    # Step 3: 連続スペースの整理
    result = re.sub(r"  +", " ", result)

    # Step 4: 保護語を復元
    for placeholder, word in placeholders.items():
        result = result.replace(placeholder, word)

    return result
