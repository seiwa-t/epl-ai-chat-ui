def detect_tags(text: str) -> list[str]:
    """テキストからタグを検出する（最大3つ）"""
    if not text:
        return []
    text_lower = text.lower()
    matched: list[str] = []
    for tag, keywords in TAG_RULES:
        if any(kw.lower() in text_lower for kw in keywords):
            matched.append(tag)
        if len(matched) >= 3:
            break
    return matched


def get_tag_icon(tag: str) -> str:
    return ICON_MAP.get(tag, "")
