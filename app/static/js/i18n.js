// ---------- 現在の言語 ----------
let _current_lang = localStorage.getItem("epl_lang") || "ja";

function get_lang() { return _current_lang; }

function t(key) {
  return (I18N[_current_lang] && I18N[_current_lang][key]) || (I18N.ja[key]) || key;
}

// ---------- DOM 全体を翻訳 ----------
function apply_i18n(lang) {
  if (lang) {
    _current_lang = lang;
    localStorage.setItem("epl_lang", lang);
  }

  // data-i18n="key" → textContent（子要素がある場合はテキストノードのみ差し替え）
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const val = t(key);
    // 子要素（spanなど）がある場合はテキストノードだけ更新して子要素を保持
    const childElements = el.querySelectorAll("*");
    if (childElements.length > 0) {
      // 最初のテキストノードを探して差し替え
      let found = false;
      for (const node of el.childNodes) {
        if (node.nodeType === Node.TEXT_NODE) {
          node.textContent = val + " ";
          found = true;
          break;
        }
      }
      if (!found) el.insertBefore(document.createTextNode(val + " "), el.firstChild);
    } else {
      el.textContent = val;
    }
  });

  // data-i18n-ph="key" → placeholder
  document.querySelectorAll("[data-i18n-ph]").forEach(el => {
    const key = el.getAttribute("data-i18n-ph");
    el.placeholder = t(key);
  });

  // data-i18n-val="key" → value（デフォルト値の言語切替）
  document.querySelectorAll("[data-i18n-val]").forEach(el => {
    const key = el.getAttribute("data-i18n-val");
    el.value = t(key);
  });

  // data-i18n-title="key" → title (ツールチップ)
  document.querySelectorAll("[data-i18n-title]").forEach(el => {
    const key = el.getAttribute("data-i18n-title");
    el.title = t(key);
  });

  // マジックワード チップ更新
  document.querySelectorAll(".magic_chip").forEach(chip => {
    const textKey = chip.getAttribute("data-i18n-mw-text");
    const wrapKey = chip.getAttribute("data-i18n-mw-wrap");
    const labelKey = chip.getAttribute("data-i18n");
    if (textKey) chip.dataset.text = t(textKey);
    if (wrapKey) chip.dataset.wrap = t(wrapKey);
    if (labelKey) chip.textContent = t(labelKey);
  });

  // html lang 属性
  document.documentElement.lang = _current_lang;

  // 言語ボタンの表示更新
  const langBtn = document.getElementById("btn_lang_toggle");
  if (langBtn) {
    langBtn.textContent = _current_lang === "ja" ? "EN" : "JA";
    langBtn.title = _current_lang === "ja" ? "Switch to English" : "日本語に切替";
  }

  // LUGJ（日本語固有機能）のグレーアウト
  const isJa = _current_lang === "ja";
  document.querySelectorAll("[data-lugj]").forEach(el => {
    if (isJa) {
      el.style.opacity = "";
      el.style.pointerEvents = "";
      el.style.filter = "";
      // input/select を有効化
      el.querySelectorAll("input,select").forEach(inp => inp.disabled = false);
    } else {
      el.style.opacity = "0.35";
      el.style.pointerEvents = "none";
      el.style.filter = "grayscale(1)";
      // input/select を無効化
      el.querySelectorAll("input,select").forEach(inp => inp.disabled = true);
    }
  });
}

function toggle_lang() {
  const next = _current_lang === "ja" ? "en" : "ja";
  localStorage.setItem("epl_lang", next);
  location.reload();
}
